class SdamGiaService:
    def __init__(self):
        pass

    def _get_task_for_number(self, num: int):
        num_str = str(num)
        if num_str in TASKS_DATABASE:
            task_data = random.choice(TASKS_DATABASE[num_str])
            return {
                "sdamgia_id": f"local_{num}_{random.randint(1000, 9999)}",
                "topic": task_data["topic"],
                "text": task_data["text"],
                "answer": task_data["answer"],
                "difficulty": 2 if num > 12 else 1,
                "tags": ["реальное задание ЕГЭ"],
                "part": 1 if num <= 12 else 2
            }
        return {
            "sdamgia_id": f"local_{num}",
            "topic": f"Задание типа {num}",
            "text": f"Решите задание типа {num}.",
            "answer": "42",
            "difficulty": 1,
            "tags": ["заглушка"],
            "part": 1 if num <= 12 else 2
        }

    async def fetch_and_cache_tasks(self, db: AsyncSession, numbers: list) -> list:
        repo = TaskRepository(db)
        tasks = []
        
        for num in numbers:
            task_data = self._get_task_for_number(num)
            task = await repo.create_task(**task_data)
            tasks.append(task)
        
        return tasks